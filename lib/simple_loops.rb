def loop_message_five_times(string)
  count = 0
  while count < 5
     puts (string)
      count += 1
    end
  end

def loop_message_n_times(message, number)
  count = 0
  while count < number
    puts message
    count += 1
  end
end

def output_array(array)
  count = 0
  while count < array.length
    puts array[count]
    count += 1
  end
end
